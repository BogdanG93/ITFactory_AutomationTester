## isdigit()
# verifica daca un string este format NUMAI din numere

## isalpha()
# verifica daca un string este format NUMAI din litere

## islower()
# verifica daca un string este format NUMAI din litere mici
# si returneaza True sau False

## isspace()
# verifica daca stringul este format NUMAI din spatii goale
# si returneaza True sau False

## isupper()
# verifica daca un string este format NUMAI din litere mari
# si returneaza True sau False

## lower()
# inlocuieste toate literele mari din string cu litere mici

## upper()
#inlocuieste toate literele mici ale stringului cu litere mari

## replace()
# returneaza o copie a stringului original in care toate aparitiile
# primului argument au fost inlocuite cu al doilea argument

## strip()
# returneaza un string fara leading space

## split()
# aceasta metoda considera ca stringurile sunt delimitate de spatii albe

## swapcase()
# returneaza un nou string in care tipul de litere este inversat
# litere mici --> litere mari si viceversa